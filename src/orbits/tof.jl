# Elliptical
function Δtₑ(a, e, μ, E, E₀)
    return (E - E₀ - e*sin(E-E₀))*(a^3/μ)^0.5
end

# Parabolic
function Δtₚ(a, e, μ, D, D₀)
    p = (a*(1 - e^2))
    return (0.5 * (p^3/μ)^0.5 * (D + D^3/3 - D₀ - D₀^3/3))
end

# Hyperbolic

# misc
function Δtt0(a, μ, M) # Time since periapsis
    return (M/2pi)*(a^3/μ)^0.5
end
